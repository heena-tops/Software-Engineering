#include<stdio.h>

int main()
{
	
	/*
		unarry operators : 
		
		increment : ++ : it increment value by 1
		decrement : -- : it decrement value by 1
	
	*/
	
	int a=10,b=0;
	
	printf("value A before : %d \n",a);
	printf("value B before : %d \n",b);
	b=a--; 
	printf("value A after : %d \n",a);
	printf("value B after : %d \n",b);

	
	
	return 0;
}
